# Re-verify or initialize the local audio encoding pipeline
class AudioPipeline(torch.nn.Module):
    def __init__(self, whisper_model_name="openai/whisper-base", target_llm_dim=4096):
        super().__init__()
        print("Initializing Local Audio Pipeline...")
        self.processor = WhisperProcessor.from_pretrained(whisper_model_name)
        self.encoder = WhisperModel.from_pretrained(whisper_model_name).encoder.to(device)
        self.encoder.eval()
        self.transcriber = WhisperForConditionalGeneration.from_pretrained(whisper_model_name).to(device)
        self.transcriber.eval()

        whisper_dim = self.encoder.config.d_model
        self.projection = torch.nn.Sequential(
            torch.nn.Linear(whisper_dim, target_llm_dim),
            torch.nn.LayerNorm(target_llm_dim),
            torch.nn.SiLU(),
            torch.nn.Dropout(0.1)
        ).to(device)
        self.projection.eval()

    def preprocess_audio(self, audio_path, target_sr=16000):
        y, sr = librosa.load(audio_path, sr=target_sr)
        y = librosa.util.normalize(y)
        y_trimmed, _ = librosa.effects.trim(y, top_db=20)
        return y_trimmed, target_sr

    @torch.no_grad()
    def forward_encoder(self, waveform):
        inputs = self.processor(waveform, return_tensors="pt", sampling_rate=16000)
        input_features = inputs.input_features.to(device)
        hidden_states = self.encoder(input_features).last_hidden_state
        projected = self.projection(hidden_states)
        return projected, hidden_states

    @torch.no_grad()
    def build_representation(self, waveform, sr, hidden_states):
        duration = len(waveform) / sr
        rms = librosa.feature.rms(y=waveform)[0]
        zcr = librosa.feature.zero_crossing_rate(y=waveform)[0]

        inputs = self.processor(waveform, return_tensors="pt", sampling_rate=16000).to(device)
        predicted_ids = self.transcriber.generate(inputs.input_features)
        transcript = self.processor.batch_decode(predicted_ids, skip_special_tokens=True)[0].strip()

        # Pool the raw 512-dim Whisper features across time to get a single spatial representation
        mean_pooled_hidden = torch.mean(hidden_states, dim=1).cpu().numpy()[0]

        return {
            "transcript": transcript if transcript else "[No speech detected]",
            "duration_seconds": round(duration, 2),
            "mean_energy": round(float(np.mean(rms)), 4),
            "max_energy": round(float(np.max(rms)), 4),
            "speaking_rate_index": round(float(np.mean(zcr)), 4),
            "audio_vector": mean_pooled_hidden # Fulfills Phase 16 Cross-Modal Memory
        }

audio_pipeline = AudioPipeline()
print("Production-ready Audio Feature Matrix compiled.")

