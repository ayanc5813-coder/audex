def text_to_speech_approximation(text_content, output_path="response.wav"):
    sr = 16000
    duration = 1.0
    t = np.linspace(0, duration, int(sr * duration), endpoint=False)
    wave = 0.4 * np.sin(2 * np.pi * 440 * t)
    sf.write(output_path, wave, sr)
    return output_path

def process_runtime_event(input_audio, user_text, target_task):
    if input_audio is None:
        return "Please upload or record an audio snippet.", None, "No operations logged."
    try:
        clean_wave, sr = audio_pipeline.preprocess_audio(input_audio)
        projected, hidden = audio_pipeline.forward_encoder(clean_wave)
        audio_stats = audio_pipeline.build_representation(clean_wave, sr, hidden)

        task_mode = engine.route_task(user_text if user_text else target_task, audio_stats)
        execution_query = user_text if user_text else f"Execute task: {target_task}"

        reply, metrics = engine.execute_unified_reasoning(execution_query, audio_stats, task_mode)
        generated_audio_file = text_to_speech_approximation(reply)

        log_view = f"--- DIAGNOSTICS ---\n{json.dumps(metrics, indent=2)}\n\n--- AUDIO PAYLOAD ---\n{json.dumps(audio_stats, indent=2)}"
        return reply, generated_audio_file, log_view

    except Exception as error:
        return f"Pipeline Error encountered: {str(error)}", None, "Execution broken."

