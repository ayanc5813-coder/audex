class SystemOrchestrator:
    def __init__(self, openrouter_key="YOUR_API_KEY", model_name="openrouter/free"):
        self.client = OpenAI(
            base_url="https://openrouter.ai/api/v1",
            api_key=openrouter_key
        )
        self.model_name = model_name
        self.memory_dim = 384
        self.index = faiss.IndexFlatL2(self.memory_dim)
        self.memory_store = []
        self.conversation_history = []
        self.logs = []

    def _get_text_embedding(self, text):
        hashed = np.zeros(self.memory_dim, dtype=np.float32)
        for i, char in enumerate(text[:self.memory_dim]):
            hashed[i % self.memory_dim] = ord(char) % 13
        norm = np.linalg.norm(hashed)
        return (hashed / norm if norm > 0 else hashed).astype('float32')

    def add_to_long_term_memory(self, user_query, assistant_response, metadata):
        record_text = f"Query: {user_query} | Response: {assistant_response}"
        vector = self._get_text_embedding(record_text).reshape(1, -1)
        self.index.add(vector)
        self.memory_store.append(record_text)

    def retrieve_memories(self, query, top_k=2):
        if self.index.ntotal == 0:
            return "No previous memories found."
        vector = self._get_text_embedding(query).reshape(1, -1)
        distances, indices = self.index.search(vector, min(top_k, self.index.ntotal))
        return "\n".join([self.memory_store[idx] for idx in indices[0] if idx < len(self.memory_store)])

    def route_task(self, user_intent, audio_stats):
        intent = user_intent.lower()
        if "transcribe" in intent: return "TRANBCRIPTION_MODE"
        elif "translate" in intent: return "TRANSLATION_MODE"
        elif "emotion" in intent: return "EMOTION_ANALYSIS_MODE"
        elif "summary" in intent: return "AUDIO_SUMMARIZATION_MODE"
        return "GENERAL_AUDIO_QA_MODE"

    def execute_unified_reasoning(self, user_query, audio_stats, task_mode):
        start_time = time.time()
        memories = self.retrieve_memories(user_query)

        prompt = f"""
        [UNIFIED AUDIO INTELLIGENCE AGENT]

        AUDIO TRANSCRIPT: "{audio_stats['transcript']}"

        TONE METADATA:
        - Duration: {audio_stats['duration_seconds']}s
        - Energy Level: {audio_stats['mean_energy']}
        - Speaking Rate (ZCR): {audio_stats['speaking_rate_index']}

        ROUTED MODE: {task_mode}
        HISTORICAL MEMORIES: {memories}

        USER INSTRUCTION: {user_query}

        Please synthesize a response. Rely primarily on the semantic meaning in the AUDIO TRANSCRIPT to answer the instruction. Use the TONE METADATA only to infer the speaker's emotional state or delivery style.
        """

        try:
            response = self.client.chat.completions.create(
                model=self.model_name,
                messages=[
                    {"role": "system", "content": "You are a robust Audio Intelligence agent. Directly answer the user's prompt based on the provided transcript and audio data."},
                    {"role": "user", "content": prompt}
                ],
                temperature=0.3
            )
            assistant_reply = response.choices[0].message.content
        except Exception as e:
            assistant_reply = f"OpenRouter API Error: {str(e)}"

        latency = time.time() - start_time
        metric_log = {"timestamp": time.time(), "latency_seconds": round(latency, 3), "query": user_query}
        self.logs.append(metric_log)
        self.add_to_long_term_memory(user_query, assistant_reply, audio_stats)

        return assistant_reply, metric_log

engine = SystemOrchestrator()
print("OpenRouter Routing & Reasoning Matrix ready.")

